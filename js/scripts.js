function courses() {
  var x = document.getElementById("tum-courses");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function hidebutton() {
    var x = document.getElementById("sidebar-id");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }